#include<iostream>
using namespace std;
int main(){
    int a[10],x,i,f=1;
    for(i=0;i<10;i++){
        cin>>a[i];
    }
    cin>>x;
    for(i=0;i<10;i++){
        if(x==a[i]){
            cout<<"The element "<<x<<" is present at index "<<i+1<<".";
            f=0;
            break;
        }
    }
    if(f!=0){
        cout<<"The number is not present in array.";
    }
}
